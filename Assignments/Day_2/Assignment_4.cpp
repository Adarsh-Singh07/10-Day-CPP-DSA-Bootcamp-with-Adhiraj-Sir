//Assignment 4 -> Create a function which will return sqrt of a number

#include<iostream>
#include<cmath>
using namespace std;

double cal_sqrt(){
    double num;
    cout << "Enter the number: ";
    cin >> num;

    if(num < 0){
        cout << "Error: Square root of negative number is not possible." << endl;
        exit(1);
    }
    return sqrt(num);
}

int main(){
    double sqrt = cal_sqrt();
    cout << "Square root of the given number is: " << sqrt << endl;

    return 0;
}
