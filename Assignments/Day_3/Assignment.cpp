/*

Assignment

Write a C++ program that demonstrates the use of pointers with simple variables. The program should :

Declare three integer variables: a, b, and c.
Declare three pointers: p1, p2, and p3.
Assign the address of a to p1, the address of b to p2, and the address of c to p3.
Use the pointers to assign values to a, b, and c.
Calculate the sum of a,b, and c using the pointers and store the result in  variable sum.
Print the values of a, b, c, and sum.

*/

#include<iostream>
using namespace std;

int main(){
    int a, b, c; // declaring three integer variables
    int *p1, *p2, *p3; // declaring three pointers

    p1 = &a; // assign the address of a to p1
    p2 = &b; // assign the address of b to p2
    p3 = &c; // assign the address of c to p3

    *p1 = 10; //using pointers to assign value to a
    *p2 = 20; //using pointers to assign value to b
    *p3 = 30; //using pointers to assign value to c

    int sum = *p1 + *p2 + *p3; //sum of a, b, and c using pointers

    cout << "Value of a: " << a << endl;
    cout << "Value of b: " << b << endl;
    cout << "Value of c: " << c << endl;
    cout << "Sum: " << sum << endl;
}
